#include "3-calc.h"

/**
 * op_add - adds
 * @a: num1
 * @b: num2
 * return: sum
 */
int op_add(int a, int b)
{
    return (a + b);
}

/**
 * op_add - subtracts
 * @a: num1
 * @b: num2
 * return: remainder
 */

int op_sub(int a, int b)
{
    return (a - b);
}

/**
 * op_add - multiply
 * @a: num1
 * @b: num2
 * return: product
 */
int op_mul(int a, int b)
{
    return (a * b);
}

/**
 * op_add - division
 * @a: num1
 * @b: num2
 * return: division
 */
int op_div(int a, int b)
{
    return (a / b);
}

/**
 * op_add - gets modulus
 * @a: num1
 * @b: num2
 * return: mod
 */
int op_mod(int a, int b)
{
    return (a % b);
}
