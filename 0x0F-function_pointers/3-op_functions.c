#include "3-calc.h"

/**
 * op_add - adds 2 numbers
 * @a: num1
 * @b: num2
 * Return: sum
 */
int op_add(int a, int b)
{
    return (a + b);
}

/**
 * op_add - subtracts
 * @a: num1
 * @b: num2
 * Return: difference
 */

int op_sub(int a, int b)
{
    return (a - b);
}

/**
 * op_add - multiply
 * @a: num1
 * @b: num2
 * Return: product
 */
int op_mul(int a, int b)
{
    return (a * b);
}

/**
 * op_add - division
 * @a: num1
 * @b: num2
 * Return: division
 */
int op_div(int a, int b)
{
    return (a / b);
}

/**
 * op_add - gets modulus
 * @a: num1
 * @b: num2
 * Return: modulus
 */
int op_mod(int a, int b)
{
    return (a % b);
}
